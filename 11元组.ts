// 数组合并了相同类型的对象，而元组合并了不同类型的对象。
// 元组起源于函数编程语言，这些语言中会频繁使用元组
let tom: [string, number] = ['tom', 25]
let Jerry: [string, number]
Jerry[0] = 'Jerry'
Jerry[1] = 20

Jerry[0].slice(1)
Jerry[1].toFixed(2)

// 当赋值或访问一个已知索引的元素时，会得到正确的类型
// 也可以只赋值其中的一项
// 但是当直接对元组类型的变量进行初始化或者赋值的时候，需要提供所有哦元组类型中指定的项
// 越界的元素，当添加越界元素时，他的类型会被限制为元组中每个类型的联合类型